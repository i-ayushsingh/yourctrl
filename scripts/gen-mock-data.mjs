// Generates src/data/mockApps.ts (dev fallback) and src-tauri/seed/apps.json (runtime
// seed) from the canonical Shortcuts/*.json files. Run with: node scripts/gen-mock-data.mjs
import { readFileSync, readdirSync, writeFileSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const shortcutsDir = join(root, "Shortcuts");
const files = readdirSync(shortcutsDir)
  .filter((f) => f.endsWith(".json"))
  .sort();

const apps = [];
for (const f of files) {
  const raw = readFileSync(join(shortcutsDir, f), "utf8");
  let data;
  try {
    data = JSON.parse(raw);
  } catch (e) {
    console.error(`SKIP ${f}: invalid JSON (${e.message})`);
    continue;
  }
  if (!data.app_name || !Array.isArray(data.shortcuts)) {
    console.error(`SKIP ${f}: missing app_name or shortcuts[]`);
    continue;
  }
  apps.push(data);
}

apps.sort((a, b) => a.app_name.localeCompare(b.app_name));

// TS dev fallback
const ts = `// AUTO-GENERATED by scripts/gen-mock-data.mjs — do not edit by hand.
// Source of truth: Shortcuts/*.json. Regenerate after editing those files.
import type { AppEntry } from "../types";

export const mockApps: AppEntry[] = ${JSON.stringify(apps, null, 2)};

export const mockAppsByName: Record<string, AppEntry> = Object.fromEntries(
  mockApps.map((a) => [a.app_name, a]),
);
`;
writeFileSync(join(root, "src", "data", "mockApps.ts"), ts);

// Tauri runtime seed
writeFileSync(
  join(root, "src-tauri", "seed", "apps.json"),
  JSON.stringify(apps, null, 2) + "\n",
);

const totalShortcuts = apps.reduce((n, a) => n + a.shortcuts.length, 0);
console.log(
  `Generated ${apps.length} apps, ${totalShortcuts} shortcuts from ${files.length} files.`,
);
